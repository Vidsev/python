# Список — это упорядоченная изменяемая коллекция элементов, которая может содержать элементы различных типов.
# В Python списки создаются с помощью квадратных скобок [].
# Они позволяют добавлять, удалять, получать элементы по индексу и выполнять множество других операций.

# создать произвольный список
my_list = [10, "abc", 3.14, [1, 2], (5, 6)]

# добавить новый элемент типа str в конец списка
my_list.append("новый строковый элемент")

# добавить новый элемент типа int на место с индексом
my_list.insert(2, 42)  # вставляем число 42 на позицию 2

# добавить новый элемент типа list в конец списка
my_list.append([7, 8, 9])

# добавить новый элемент типа tuple на место с индексом
my_list.insert(4, (100, 200))

# получить элемент по индексу
element_at_index_3 = my_list[3]
print("Элемент на индексе 3:", element_at_index_3)

# удалить элемент
my_list.remove("abc")

# найти число повторений элемента списка
count_20 = my_list.count(20)
print("Количество повторений числа 20:", count_20)


# 2. работа со словарем

# Словарь — это неупорядоченная коллекция пар «ключ — значение», где ключи должны быть уникальными и неизменяемыми (например, строки, числа, кортежи).
#  Они позволяют быстро получать, добавлять или удалять элементы по ключу.

# создать произвольный словарь
my_dict = {'ключ1': 10, (1, 2): [1, 2, 3], 'ключ2': 20}

# добавить новый элемент с ключом типа str и значением типа int
my_dict['новый_ключ'] = 100

# добавить новый элемент с ключом типа кортеж и значением типа список
my_dict[(3, 4)] = ['a', 'b', 'c']

# получить элемент по ключу
value = my_dict.get('ключ1')
print("значение по ключу 'ключ1':", value)

# удалить элемент по ключу
del my_dict['ключ2']

# получить список ключей словаря
keys_list = list(my_dict.keys())
print("ключи словаря:", keys_list)


# 3. вывод нечетных чисел до 139

# Это задание — фильтрация чисел из списка, при которой выводятся только нечетные числа, и процесс останавливается при встрече с числом 139.
#  Используется цикл for и условие if для проверки условий.

numbers = [10, 15, 20, 139, 25, 30, 45]

for num in numbers:
    if num == 139:
        break
    if num % 2 != 0:
        print(num)


# 4. элементы, меньше 30 и делящиеся на 3, и сумма остальных

# Задача — выбрать из списка элементы, которые удовлетворяют двум условиям: меньше 30 и делятся на 3 без остатка.
#  Все остальные элементы суммируются, и итоговая сумма выводится.
#  В основном, это операции фильтрации и подсчета суммы.

lst = [11, 5, 8, 32, 15, 3, 20, 132, 21, 4, 555, 9, 20]
sum_rest = 0

for item in lst:
    if item < 30 and item % 3 == 0:
        print(item)
    else:
        sum_rest += item

print("Сумма остальных элементов:", sum_rest)


# 5. функции def month_to_season()

# Функция — это блок кода, который выполняет определенную задачу. 
# В данном случае, функция month_to_season() принимает номер месяца (от 1 до 12)
#  и возвращает название сезона (зима, весна, лето, осень), к которому относится этот месяц, основываясь на календарных сроках.

def month_to_season(month):
    if month in [12, 1, 2]:
        return 'зима'
    elif month in [3, 4, 5]:
        return 'весна'
    elif month in [6, 7, 8]:
        return 'лето'
    elif month in [9, 10, 11]:
        return 'осень'
    else:
        return 'Некорректный номер месяца'

# пример использования
print(month_to_season(2))  # выведет 'зима'